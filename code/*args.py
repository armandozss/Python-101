bre = "----------------------------------------------"
end = "This is all of args"

#-----------------------------------------------------------------------------------------------------------------

# Without args

def add(num1, num2):
    sum = num1 + num2
    return sum

print(add(2, 5))

# Break
print(" ")
print(bre)

#-----------------------------------------------------------------------------------------------------------------

# With args

def math(*args):
    sum = 0
    for i in args:
        sum += i
    return sum

print(math(5, 7, 8))

#-----------------------------------------------------------------------------------------------------------------

# End
print(" ")
print(bre)
print(end)

