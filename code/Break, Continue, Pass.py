bre = "----------------------------------------------"
end = "This is all of Break, Continue, Pass"

#-----------------------------------------------------------------------------------------------------------------

# Break
while True:
    name = input("What is your name?: ")
    if name != "":
        print("Hello!!, " + name)
        break

# Break
print(" ")
print(bre)

#-----------------------------------------------------------------------------------------------------------------

# Continue
Phone_number = "+1-456-8595-456"

for i in Phone_number:
    if i == "-":
        continue
    print(i, end="")

# Break
print(" ")
print(bre)

#-----------------------------------------------------------------------------------------------------------------

# Pass
for i in range (1, 6):
    if i == 3:
        pass
    else:
        print(i, end=" ")

#-----------------------------------------------------------------------------------------------------------------

# End
print(" ")
print(bre)
print(end)

